package rvstd.iso.com.recyclerviewswipetodelete;

public class Coche {

    private int img;
    private String nombre;

    public Coche(int img, String nombre) {
        this.img = img;
        this.nombre = nombre;
    }

    public int getImg() {
        return img;
    }

    public String getNombre() {
        return nombre;
    }
}
